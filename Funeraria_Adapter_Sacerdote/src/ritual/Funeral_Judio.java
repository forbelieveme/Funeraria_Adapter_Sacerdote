package ritual;

public class Funeral_Judio extends Judaismo {

	@Override
	public String orar() {
		return "\"Dios, lleno de misericordia, que moras en lo alto, en las "
				+ "\n alturas, otorga perfecto descanso bajos las protectoras "
				+ "\n alas de tu Presencia Divinaa [...] Por lo tanto, el Maestro "
				+ "\n de la Misericordia lo protegerá para siempre, desde atrás del "
				+ "\n escondite de sus alas, y atará su alma con la cuerda de la vida. "
				+ "\n La Eternidad es su herencia, y él descansará pacíficamente en "
				+ "\n su lugar de reposo, y dígase: Amén\".";
	}

	@Override
	public String bendecir() {
		return "Que Dios te haga como Efraim y Menashé";
	}

	@Override
	public String descripcionRitual() {
		return "Ritual Judio";
	}

}
