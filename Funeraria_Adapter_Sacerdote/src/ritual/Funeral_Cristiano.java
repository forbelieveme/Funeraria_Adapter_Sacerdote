package ritual;

public class Funeral_Cristiano extends Cristianismo {

	@Override
	public String orar() {
		return "“Oí una voz que desde el cielo me decía: Escribe: Bienaventurados "
				+ "\n de aquí en adelante los muertos que mueren en el Señor. Sí, dice "
				+ "\n el Espíritu, descansarán de sus trabajos, porque sus obras con "
				+ "\n ellos siguen” (Apocalipsis 14:13).";
	}

	@Override
	public String bendecir() {
		return "Bendito seas en nombre del señor.";
	}

	@Override
	public String descripcionRitual() {
		return "Ritual Cristiano";
	}

}
